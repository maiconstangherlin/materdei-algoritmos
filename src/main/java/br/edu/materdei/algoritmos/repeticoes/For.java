package br.edu.materdei.algoritmos.repeticoes;

public class For {

    public static void main(String[] args) {
        
        // 1º parte: declaração da variável de controle
        // 2º parte: expressão para condição de repetição
        // 3º parte: incremento da variável de controle
        for (int i = 0; i < 10; i++) {
            System.out.println("Índice: " + i);
        }
        
    }
    
}
