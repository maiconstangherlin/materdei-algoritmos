package br.edu.materdei.algoritmos.variaveis;

public class Declaracao {

    public static void main(String[] args) {
        
        // Sintaxe de declaração recebendo um valor
        // tipo | nome = valor;
        int numero = 1;         
        
        
        
        
        // Sintaxe de declaração sem passar valor inicial
        // tipo | nome;
        String texto;
        
        // atribuindo valor após a criação
        // nome = valor;
        texto = "Hello World";        
    }
}
